#include <iostream>

int main()
{
  int x = 5;  // Integer (whole number)
  int y = 10; // Integer (whole number)

  std::cout << "x == y: " << (x == y) << std::endl; // Equal to
  std::cout << "x != y: " << (x != y) << std::endl; // Not equal to
  std::cout << "x > y: " << (x > y) << std::endl;   // Greater than
  std::cout << "x < y: " << (x < y) << std::endl;   // Less than
}