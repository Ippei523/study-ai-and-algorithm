#include <iostream>

int main()
{
  int myNum = 5;                   // Integer (whole number)
  std::cout << myNum << std::endl; // Output: 5

  int myNum2;
  myNum2 = 10;                      // Assigning a value to the variable
  std::cout << myNum2 << std::endl; // Output: 10

  myNum2 = 25;                      // Changing the value of the variable
  std::cout << myNum2 << std::endl; // Output: 25

  int myAge = 18;                // Integer (whole number)
  std::cout << "My age is: " << myAge << std::endl; // Output: My age is: 18

  int x = 5; // Integer (whole number)
  int y = 10; // Integer (whole number)
  int z = x + y; // Integer (whole number)
  std::cout << "The sum of x and y is: " << z << std::endl; // Output: The sum of x and y is: 15

  return 0;
}