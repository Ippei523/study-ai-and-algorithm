#include <iostream>

int main()
{
  char a = 65, b = 66, c = 67;

  std::cout << "a: " << a << std::endl;
  std::cout << "b: " << b << std::endl;
  std::cout << "c: " << c << std::endl;
  std::cout << "a + b: " << a + b << std::endl;
  std::cout << "a + c: " << a + c << std::endl;
  std::cout << "b + c: " << b + c << std::endl;

  return 0;
}